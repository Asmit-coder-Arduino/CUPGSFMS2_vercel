function fmt(v: number | null) {
  return v != null ? v.toFixed(2) : "N/A";
}

function ratingColor(v: number | null): [number, number, number] {
  if (v == null) return [150, 150, 150];
  if (v >= 4) return [0, 128, 0];
  if (v >= 3) return [200, 140, 0];
  return [200, 0, 0];
}

