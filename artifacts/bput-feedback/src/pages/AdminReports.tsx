function drawStatBoxes(doc: jsPDF, stats: { label: string; value: string; sub?: string }[], y: number): number {
  const pw = doc.internal.pageSize.getWidth();
  const margin = 15;
  const gap = 4;
  const boxW = (pw - margin * 2 - gap * (stats.length - 1)) / stats.length;

  stats.forEach((s, i) => {
    const x = margin + i * (boxW + gap);
    doc.setFillColor(245, 250, 255);
    doc.setDrawColor(180, 210, 240);
    doc.setLineWidth(0.4);
    doc.roundedRect(x, y, boxW, 22, 2, 2, "FD");
    doc.setFontSize(14);
    doc.setFont("helvetica", "bold");
    doc.setTextColor(...BPUT_BLUE);
    doc.text(s.value, x + boxW / 2, y + 11, { align: "center" });
    doc.setFontSize(7);
    doc.setFont("helvetica", "normal");
    doc.setTextColor(80, 80, 80);
    doc.text(s.label, x + boxW / 2, y + 17, { align: "center" });
    if (s.sub) {
      doc.setFontSize(6.5);
      doc.setTextColor(130, 130, 130);
      doc.text(s.sub, x + boxW / 2, y + 21, { align: "center" });
    }
  });

  doc.setTextColor(0, 0, 0);
  return y + 28;
}

function drawHorizontalBar(doc: jsPDF, label: string, value: number | null, x: number, y: number, barWidth: number) {
  const color = ratingColor(value);
  const pct = value != null ? (value / 5) : 0;
  const filledW = barWidth * pct;

  doc.setFontSize(8.5);
  doc.setFont("helvetica", "normal");
  doc.setTextColor(60, 60, 60);
  doc.text(label, x, y);

  doc.setFillColor(225, 225, 225);
  doc.roundedRect(x + 68, y - 3.5, barWidth, 5, 1.5, 1.5, "F");

  if (filledW > 0) {
    doc.setFillColor(...color);
    doc.roundedRect(x + 68, y - 3.5, filledW, 5, 1.5, 1.5, "F");
  }

  doc.setFontSize(8);
  doc.setFont("helvetica", "bold");
  doc.setTextColor(...color);
  doc.text(fmt(value) + " / 5", x + 68 + barWidth + 4, y);
  doc.setTextColor(0, 0, 0);
}

function drawDeptBarChart(doc: jsPDF, depts: { code: string; avgOverall: number | null }[], x: number, y: number, chartW: number, chartH: number) {
  const pw = doc.internal.pageSize.getWidth();
  const barCount = depts.length;
  const maxVal = 5;
  const barW = (chartW - 10) / barCount - 4;
  const scale = chartH / maxVal;

  doc.setDrawColor(210, 210, 210);
  doc.setLineWidth(0.3);
  for (let v = 0; v <= 5; v++) {
    const lineY = y + chartH - v * scale;
    doc.line(x, lineY, x + chartW, lineY);
    doc.setFontSize(6);
    doc.setTextColor(140, 140, 140);
    doc.text(v.toString(), x - 4, lineY + 1, { align: "right" });
  }

  depts.forEach((d, i) => {
    const barX = x + i * (barW + 4) + 5;
    const val = d.avgOverall ?? 0;
    const barH = val * scale;
    const barY = y + chartH - barH;

    const col = ratingColor(d.avgOverall);
    doc.setFillColor(...col);
    doc.rect(barX, barY, barW, barH, "F");

    doc.setFontSize(6.5);
    doc.setFont("helvetica", "bold");
    doc.setTextColor(...col);
    if (val > 0) doc.text(val.toFixed(1), barX + barW / 2, barY - 1.5, { align: "center" });
    doc.setFont("helvetica", "normal");
    doc.setTextColor(60, 60, 60);
    doc.text(d.code, barX + barW / 2, y + chartH + 5, { align: "center" });
  });
  doc.setTextColor(0, 0, 0);
}

